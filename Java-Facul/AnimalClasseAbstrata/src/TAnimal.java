
public abstract class TAnimal {
	protected String raca, cor, porte, Mostra;
	
	abstract protected void setRaca(String Raca);
	abstract protected void setCor(String Color);
	abstract protected void setPorte(String Porte);
	abstract protected void Mostratudo();
	
	abstract protected String getRaca();
	abstract protected String getCor();
	abstract protected String getPorte();
	
}
